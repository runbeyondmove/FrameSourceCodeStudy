package com.sourcecodestudy.spring.samples;

/**
 * 用于依赖注入中描述bean依赖
 */
public class FormatCode {
	private String beanName;

	public FormatCode(String beanName) {
		super();
		this.beanName = beanName;
	}

	/**
	 * 获得引用的beanName
	 */
	public String getBeanName() {
		//Class<T>
		return this.beanName;
	}
}
